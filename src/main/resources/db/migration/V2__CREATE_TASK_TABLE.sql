  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   date date NOT NULL,
   title VARCHAR(255),
   description VARCHAR(255) NOT NULL,
   status SMALLINT NOT NULL,
   user_id BIGINT NOT NULL,
   CONSTRAINT pk_tasks PRIMARY KEY (id)
);

